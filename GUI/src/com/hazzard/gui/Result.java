    Result(int lMin, int lSec, int lMs, int rMin, int rSec, int rMs) {
        this();
        this.lMin = lMin;
        this.lSec = lSec;
        this.lMs = lMs;
        this.rMin = rMin;
        this.rSec = rSec;
        this.rMs = rMs;
        if(lMin<rMin){
            jTextField2.setText("Player ONE");
            winMin = lMin;
            winSec = lSec;
            winMs = lMs;                   
        }else if(lMin==rMin){
            if(lSec<rSec){
                jTextField2.setText("Player ONE");
                winSec = lSec;
                winMs = lMs;
            }else if(lSec==rSec){
                if(lMs<rMs){
                    jTextField2.setText("Player ONE");
                    winMs = lMs;
                }else if(lMs==rMs){                        
                    jTextField2.setText("TIE");
                }else{
                    jTextField2.setText("Player TWO");
                    winMin = rMin;
                    winSec = rSec;
                    winMs = rMs;
                }
            }else{
                jTextField2.setText("Player TWO");
                winMin = rMin;
                winSec = rSec;
                winMs = rMs;
            }
        }else{
            jTextField2.setText("Player TWO");
            winMin = rMin;
            winSec = rSec;
            winMs = rMs;
        }
        
        if(lMin<10){
            jTextField7.setText("0"+Integer.toString(lMin));
        }else{
            jTextField7.setText(Integer.toString(lMin));
        }
        if(lSec<10){
            jTextField8.setText("0"+Integer.toString(lSec));
        }else{
            jTextField8.setText(Integer.toString(lSec));
        }
        if(lMs<10){
            jTextField6.setText("00"+Integer.toString(lMs));
        }else if(lMs<100){
            jTextField6.setText("0"+Integer.toString(lMs));
        }else{
            jTextField6.setText(Integer.toString(lMs));
        }

        if(rMin<10){
            jTextField10.setText("0"+Integer.toString(rMin));
        }else{
            jTextField10.setText(Integer.toString(rMin));
        }
        if(rSec<10){
            jTextField11.setText("0"+Integer.toString(rSec));
        }else{
            jTextField11.setText(Integer.toString(rSec));
        }
        if(rMs<10){
            jTextField9.setText("00"+Integer.toString(rMs));
        }else if(rMs<100){
            jTextField9.setText("0"+Integer.toString(rMs));
        }else{
            jTextField9.setText(Integer.toString(rMs));
        }       
    }

